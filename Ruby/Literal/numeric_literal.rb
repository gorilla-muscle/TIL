# 正の整数リテラル
+12

# 負の整数リテラル
-12

# 少数リテラル
0.1

# 指数表記
3.0e2 # => 300.0(3.0に10の2乗を乗算)

3.0e-2 # => 0.03(3.0に10のマイナス2乗を乗算)

# 2進数リテラル
0b10 # => 2(10の2進数表現)

# 8進数リテラル
0o110 # => 72(10進数110の8進数表現)

# 10進数リテラル
0d19 # => 19(19の10進数表現)

# 16進数リテラル
0x1B # => 27(1Bは27の16進数表現)

# _の使用
## Rubyインタプリタは、_を無視する
100_000_000 # => 100000000

# 有理数、複素数のリテラル
42/10r # => (21/5)
(42/10r).class # => Rational
3.14r # => (157/50)
42i # => (0+42i)
42i.class # => Complex
3.14i # => (0+3.14i)
